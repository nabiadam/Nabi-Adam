import turtle
wn = turtle.Screen()    
wn.title("Tess & Alex")

tess = turtle.Turtle()       
tess.pensize(5)

alex = turtle.Turtle()      

for i in [0,1,2,3]:
        tess.forward(300)
        tess.left(120)          
for i in [0,1,2,3,4]:
        alex.forward(200)
        alex.left(90)

wn.mainloop()
    

        
        




	




